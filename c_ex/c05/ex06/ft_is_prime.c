int	ft_is_prime(int nb)
{
	int	i;
	int	ct;

	i = 2;
	ct = 0;
	while (i <= nb / i)
	{
		if (nb % i == 0)
		{
			ct++;
			break ;
		}
		i++;
	}
	if (ct == 0)
		return (1);
	return (0);
}
